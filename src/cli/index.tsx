      console.log(chalk.green(`✔ Initialized open-styles for: ${projectPath}`));
      if (opts.style) {
        console.log(chalk.dim(`  Active style: ${opts.style}`));
      }
      if (existsSync(claudeDir)) {
        console.log(chalk.dim("  Hook injected into .claude/settings.json"));
      }
